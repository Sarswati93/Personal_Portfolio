// Mobile Navigation Toggle
const hamburger = document.querySelector('.hamburger');
const navLinks = document.querySelector('.nav-links');
const links = document.querySelectorAll('.nav-links li');

hamburger.addEventListener('click', () => {
    // Toggle Nav
    navLinks.classList.toggle('active');
    hamburger.classList.toggle('active');
    
    // Animate Links
    links.forEach((link, index) => {
        if (link.style.animation) {
            link.style.animation = '';
        } else {
            link.style.animation = `navLinkFade 0.5s ease forwards ${index / 7 + 0.3}s`;
        }
    });
});

// Close mobile navigation when clicking on a link
links.forEach(link => {
    link.addEventListener('click', () => {
        navLinks.classList.remove('active');
        hamburger.classList.remove('active');
        links.forEach(link => {
            link.style.animation = '';
        });
    });
});

// Smooth scrolling for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
        e.preventDefault();
        
        const targetId = this.getAttribute('href');
        const targetElement = document.querySelector(targetId);
        
        window.scrollTo({
            top: targetElement.offsetTop - 80,
            behavior: 'smooth'
        });
    });
});

// Sticky header on scroll
window.addEventListener('scroll', () => {
    const header = document.querySelector('header');
    header.classList.toggle('sticky', window.scrollY > 0);
});

// Animation on scroll
window.addEventListener('scroll', () => {
    const skills = document.querySelector('.skills');
    const skillPosition = skills.getBoundingClientRect().top;
    const screenPosition = window.innerHeight / 1.2;
    
    if (skillPosition < screenPosition) {
        document.querySelector('.html-css').style.width = '90%';
        document.querySelector('.javascript').style.width = '85%';
        document.querySelector('.react').style.width = '80%';
        document.querySelector('.communication').style.width = '95%';
        document.querySelector('.teamwork').style.width = '90%';
        document.querySelector('.problem-solving').style.width = '85%';
    }
});

